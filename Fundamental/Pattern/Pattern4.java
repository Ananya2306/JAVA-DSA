import java.util.*;
public class Pattern4 {
    public static void main(String[] args) {

        Scanner sc = new Scanner(System.in);
        System.out.print("Enter a number :- ");
        int n = sc.nextInt();
        //row
        for (int i = 1; i <= n; i++) {
            //column
            for (int j = 1; j <= n; j++) {
                System.out.print(n);
            }
          System.out.println();
    

        }
        sc.close();
    }
}
